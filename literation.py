#iteration over a list
fruits=['apple','banana','cherry']
for fruit in fruits:
    print(fruits)
#iteration over a string
for letter in 'banana':
    print(letter)
#iteration over a range
for i in range(5):
    print(i)
#iteration over a list of numbers
for i in [1,2,3,4,5]:
    print(i)
#iteration over a list of tuples
for i in [(1,2),(3,4),(5,6)]:
    print(i)
